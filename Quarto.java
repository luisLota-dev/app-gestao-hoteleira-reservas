public class Quarto {
    public int id;
    public int numero;
    public int capacidade;
    public boolean estaOcupado;

    public Quarto(int id, int numero, int capacidade, boolean estaOcupado) {
        this.id = id;
        this.numero = numero;
        this.capacidade = capacidade;
        this.estaOcupado = estaOcupado;
    }

}